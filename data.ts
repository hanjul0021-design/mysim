// Transform the configuration data into usable application data
export const LESSONS: LessonData[] = LESSONS_CONFIG.map((config, index) => {
  const quizSegments: QuizSegment[] = [];
  
  config.quizRawText.forEach((line, lineIdx) => {
    let verseNum: number | undefined;
    let cleanLine = line;

    // Extract verse number [18절]
    const match = line.match(/^\[(\d+)절?\]\s*/);
    if (match) {
      verseNum = parseInt(match[1], 10);
      cleanLine = line.replace(match[0], '');
    }

    // Split by {}
    const parts = cleanLine.split(/({[^}]+})/g);
    
    let isFirstInLine = true;
    parts.forEach((part, partIdx) => {
      if (!part) return; // Skip empty parts
      
      const isBlank = part.startsWith('{') && part.endsWith('}');
      const text = isBlank ? part.slice(1, -1) : part;
      
      quizSegments.push({
        id: `l${index}-line${lineIdx}-p${partIdx}`,
        text,
        isBlank,
        verseNum: isFirstInLine ? verseNum : undefined
      });
      isFirstInLine = false;
    });
  });

  return {
    config,
    passage: {
      reference: `${config.passageInfo.book} ${config.passageInfo.chapter}`,
      verses: config.verses
    },
    qna: config.qna.map((q, idx) => ({
      id: idx + 1,
      question: q.question,
      answer: q.answer
    })),
    quiz: quizSegments
  };
});

